'''
8. Conversor de Temperatura:
• Peça a temperatura em Celsius.
• Converta a temperatura para Fahrenheit.
• Exiba a temperatura em Fahrenheit.
'''
celsius = float(input('Digite uma temperatura em celsius: '))
fahrenheit = celsius * 1.8 + 32

print(f'A temperatura em Celsius {celsius} equivale a {fahrenheit} Fahrenheit')
